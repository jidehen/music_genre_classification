class Track:
    def __init__(self, id, title, artist, features, track_listens, album_listens, favorites, interests):
        self.id = id
        self.title = title
        self.artist = artist
        self.features = features
        self.track_listens = track_listens
        self.album_listens = album_listens
        self.favorites = favorites
        self.interests = interests
